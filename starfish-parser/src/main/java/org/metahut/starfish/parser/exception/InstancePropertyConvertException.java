package org.metahut.starfish.parser.exception;

/**
 *
 */
public class InstancePropertyConvertException extends AbstractMetaParserException {

    public InstancePropertyConvertException() {
    }

    public InstancePropertyConvertException(String message) {
        super(message);
    }

    public InstancePropertyConvertException(String message, Throwable cause) {
        super(message, cause);
    }

    public InstancePropertyConvertException(Throwable cause) {
        super(cause);
    }

    public InstancePropertyConvertException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
